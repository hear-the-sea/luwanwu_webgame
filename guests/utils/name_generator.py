# 姓氏权重配置（用于加权随机）
# 常见姓氏出现概率更高，稀有姓氏更少见
SURNAME_WEIGHTS: List[int] = (
    [15] * 60 +      # 常见单姓：权重 15（总权重 900，约63%）
    [3] * 100 +      # 补充单姓：权重 3（总权重 300，约21%）
    [2] * 60 +       # 历史单姓：权重 2（总权重 120，约8.5%）
    [1] * 40 +       # 地域姓氏：权重 1（总权重 40，约3%）
    [1] * 28 +       # 古代姓氏：权重 1（总权重 28，约2%）
    [1] * 32         # 复姓：权重 1（总权重 32，约2.5%）
)
# 总权重：1420
# 概率分布：常见姓 63% | 补充单姓 21% | 历史姓 8.5% | 其他 7.5%


def generate_random_name(rng: random.Random) -> str:
    """
    生成随机的古风姓名。
    
    支持多种模式：
    - 单字名（如：王勇、李明）
    - 双字名（如：张云飞、赵子龙）
    - 复姓名（如：司马懿、诸葛亮）
    
    姓氏采用加权随机：
    - 常见姓氏（如李、王、张）出现概率最高（约63%）
    - 补充单姓次之（约21%）
    - 历史姓氏、复姓等较少见（约16%）
    
    Args:
        rng: 随机数生成器
        
    Returns:
        生成的姓名字符串
        
    Examples:
        >>> import random
        >>> rng = random.Random(42)
        >>> generate_random_name(rng)
        '王云飞'
    """
    # 使用加权随机选择姓氏
    surname = rng.choices(BLACK_SURNAMES, weights=SURNAME_WEIGHTS, k=1)[0]

    # 根据姓氏长度调整名字生成策略
    is_compound_surname = len(surname) > 1

    # 名字结构概率分布
    # 复姓更倾向单字名，单姓则单双字均衡
    if is_compound_surname:
        # 复姓：70% 单字名，30% 双字名
        use_single_char = rng.random() < 0.7
    else:
        # 单姓：40% 单字名，60% 双字名
        use_single_char = rng.random() < 0.4

    if use_single_char:
        # 单字名
        given = rng.choice(BLACK_GIVEN)
    else:
        # 双字名
        first_char = rng.choice(BLACK_GIVEN)

        # 10% 概率生成叠字名（如：云云、山山）
        if rng.random() < 0.1:
            given = first_char + first_char
        else:
            # 避免与第一个字相同
            second_choices = [c for c in BLACK_GIVEN if c != first_char]
            if second_choices:
                second_char = rng.choice(second_choices)
            else:
                second_char = rng.choice(BLACK_GIVEN)
            given = first_char + second_char

    return surname + given
