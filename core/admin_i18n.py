def apply_model_field_labels(model: type[Model], labels: Mapping[str, str]) -> None:
    for field_name, verbose_name in labels.items():
        try:
            field = model._meta.get_field(field_name)
        except FieldDoesNotExist:
            continue
        setattr(field, "verbose_name", verbose_name)


def apply_common_field_labels(*models: type[Model], labels: Mapping[str, str] | None = None) -> None:
    merged_labels = dict(COMMON_ADMIN_FIELD_LABELS)
    if labels:
        merged_labels.update(labels)
    for model in models:
        apply_model_field_labels(model, merged_labels)


def apply_model_property_labels(model: type[Model], labels: Mapping[str, str]) -> None:
    for attr_name, short_description in labels.items():
        attr = getattr(model, attr_name, None)
        if isinstance(attr, property) and attr.fget is not None:
            setattr(attr.fget, "short_description", short_description)
        elif callable(attr):
            setattr(attr, "short_description", short_description)
